// Utilizamos un bucle for para iterar desde 1 hasta 10 (inclusive)
    // Mostramos el encabezado de la tabla
    // Utilizamos otro bucle for para generar los multiplicadores del 1 al 10
      // Calculamos el resultado de la multiplicación
      // Mostramos la multiplicación y su resultado
    // Agregamos una línea en blanco para separar las tablas


for (let i = 1; i <= 10; i++) {

    console.log(`Tabla de multiplicar del ${i}:`);

    for (let j = 1; j <= 10; j++) {
      let resultado = i * j;
      console.log(`${i} x ${j} = ${resultado}`);
    }
    console.log();
  }

